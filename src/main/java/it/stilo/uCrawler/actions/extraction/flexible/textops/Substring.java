/**
 *
 * @author stilo
 */
public class Substring implements PerformerIF {

	private int start = 0;
	private int end = -1;

	@Override
	public Object performOp(Object obj) throws PerformerException {
		if (obj == null)
			return null;
		else {
			String s = obj.toString();
			if (end < 0)
				end = s.length();
			return s.substring(start, end);
		}
	}

	/**
	 * @return the start
	 */
	public int getStart() {
		return start;
	}

	/**
	 * @param start the start to set
	 */
	public void setStart(int start) {
		this.start = start;
	}

	/**
	 * @return the end
	 */
	public int getEnd() {
		return end;
	}

	/**
	 * @param end the end to set
	 */
	public void setEnd(int end) {
		this.end = end;
	}

}
